        xs[32] = vs[32].v_exp(m_k_x_inter_B_B_0, coords_B_3_b, coords_B_6_c);
        xs[33] = vs[33].v_exp(m_k_x_inter_B_B_0, coords_B_4_b, coords_B_5_c);
        xs[34] = vs[34].v_exp(m_k_x_inter_B_B_0, coords_B_4_b, coords_B_6_c);
