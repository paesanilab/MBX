double x3b_v2x::eval(const double* w1, const double* w2, const double* w3, const size_t nt) {
    std::vector<double> energy(nt, 0.0);
    double rabsq[nt], racsq[nt], rbcsq[nt];
    double rab[nt], rac[nt], rbc[nt];
    double dab[3 * nt], dac[3 * nt], dbc[3 * nt];

    for (size_t i = 0; i < nt; i++) {
        for (size_t j = 0; j < 3; j++) {
            dab[3 * i + j] = w1[9 * i + j] - w2[9 * i + j];
            dac[3 * i + j] = w1[9 * i + j] - w3[9 * i + j];
            dbc[3 * i + j] = w2[9 * i + j] - w3[9 * i + j];
        }

        rabsq[i] = 0.0;
        racsq[i] = 0.0;
        rbcsq[i] = 0.0;
        for (size_t j = 0; j < 3; j++) {
            rabsq[i] += dab[3 * i + j] * dab[3 * i + j];
            racsq[i] += dac[3 * i + j] * dac[3 * i + j];
            rbcsq[i] += dbc[3 * i + j] * dbc[3 * i + j];
        }

        rab[i] = std::sqrt(rabsq[i]);
        rac[i] = std::sqrt(racsq[i]);
        rbc[i] = std::sqrt(rbcsq[i]);

        bool skip = (rab[i] > r3f && rac[i] > r3f) || (rab[i] > r3f && rbc[i] > r3f) || (rac[i] > r3f && rbc[i] > r3f);
        if (skip) continue;

        double vv[36];
        size_t sh = i;
        size_t sh9 = 9 * sh;

        const double* Oa = w1 + sh9;
        const double* Ha1 = w1 + sh9 + 3;
        const double* Ha2 = w1 + sh9 + 6;

        const double* Ob = w2 + sh9;
        const double* Hb1 = w2 + sh9 + 3;
        const double* Hb2 = w2 + sh9 + 6;

        const double* Oc = w3 + sh9;
        const double* Hc1 = w3 + sh9 + 3;
        const double* Hc2 = w3 + sh9 + 6;

        double x[36];

        x[0] = var(kHH_intra, dHH_intra, Ha1, Ha2);
        x[1] = var(kHH_intra, dHH_intra, Hb1, Hb2);
        x[2] = var(kHH_intra, dHH_intra, Hc1, Hc2);
        x[3] = var(kOH_intra, dOH_intra, Oa, Ha1);
        x[4] = var(kOH_intra, dOH_intra, Oa, Ha2);
        x[5] = var(kOH_intra, dOH_intra, Ob, Hb1);
        x[6] = var(kOH_intra, dOH_intra, Ob, Hb2);
        x[7] = var(kOH_intra, dOH_intra, Oc, Hc1);
        x[8] = var(kOH_intra, dOH_intra, Oc, Hc2);

        x[9] = var(kHH, dHH, Ha1, Hb1);
        x[10] = var(kHH, dHH, Ha1, Hb2);
        x[11] = var(kHH, dHH, Ha1, Hc1);
        x[12] = var(kHH, dHH, Ha1, Hc2);
        x[13] = var(kHH, dHH, Ha2, Hb1);
        x[14] = var(kHH, dHH, Ha2, Hb2);
        x[15] = var(kHH, dHH, Ha2, Hc1);
        x[16] = var(kHH, dHH, Ha2, Hc2);
        x[17] = var(kHH, dHH, Hb1, Hc1);
        x[18] = var(kHH, dHH, Hb1, Hc2);
        x[19] = var(kHH, dHH, Hb2, Hc1);
        x[20] = var(kHH, dHH, Hb2, Hc2);
        x[21] = var(kOH, dOH, Oa, Hb1);
        x[22] = var(kOH, dOH, Oa, Hb2);
        x[23] = var(kOH, dOH, Oa, Hc1);
        x[24] = var(kOH, dOH, Oa, Hc2);
        x[25] = var(kOH, dOH, Ob, Ha1);
        x[26] = var(kOH, dOH, Ob, Ha2);
        x[27] = var(kOH, dOH, Ob, Hc1);
        x[28] = var(kOH, dOH, Ob, Hc2);
        x[29] = var(kOH, dOH, Oc, Ha1);
        x[30] = var(kOH, dOH, Oc, Ha2);
        x[31] = var(kOH, dOH, Oc, Hb1);
        x[32] = var(kOH, dOH, Oc, Hb2);
        x[33] = var(kOO, dOO, Oa, Ob);
        x[34] = var(kOO, dOO, Oa, Oc);
        x[35] = var(kOO, dOO, Ob, Oc);

        double e3b = poly_3b_v2x::eval(thefit, x);

        energy[i] = e3b;
    }

    double e = 0.0;
    for (size_t i = 0; i < nt; i++) {
        double gab, gac, gbc;

        const double sab = f_switch(rab[i], gab);
        const double sac = f_switch(rac[i], gac);
        const double sbc = f_switch(rbc[i], gbc);

        const double s = sab * sac + sab * sbc + sac * sbc;

        e += s * energy[i];
    }
    return e;
}

