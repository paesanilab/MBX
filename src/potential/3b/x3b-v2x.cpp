double var(const double& k, const double& r0, const double* a1, const double* a2) {
    const double dx[3] = {a1[0] - a2[0], a1[1] - a2[1], a1[2] - a2[2]};

    const double dsq = dx[0] * dx[0] + dx[1] * dx[1] + dx[2] * dx[2];
    const double d = std::sqrt(dsq);

    return std::exp(-k * (d - r0));
}

//----------------------------------------------------------------------------//

void g_var(const double& g, const double& k, const double& r0, const double* a1, const double* a2, double* g1,
           double* g2) {
    const double dx[3] = {a1[0] - a2[0], a1[1] - a2[1], a1[2] - a2[2]};

    const double dsq = dx[0] * dx[0] + dx[1] * dx[1] + dx[2] * dx[2];
    const double d = std::sqrt(dsq);

    const double gg = -k * g * std::exp(-k * (d - r0)) / d;

    for (int i = 0; i < 3; ++i) {
        g1[i] += gg * dx[i];
        g2[i] -= gg * dx[i];
    }
}

