double x2b_h2o_ion_v2x::eval(const double* w1, const double* x, double* g1, double* g2, const size_t nd,
                             std::vector<double>* virial) {
#ifdef DEBUG
    std::cerr << std::scientific << std::setprecision(10);
    std::cerr << "\nEntering " << __func__ << " in " << __FILE__ << std::endl;
    std::cerr << "Input coordinates (1) for " << nd << " monomers:\n";
    for (size_t i = 0; i < nd; i++) {
        for (size_t j = 0; j < 9; j++) {
            std::cerr << w1[9 * i + j] << " , ";
        }
        std::cerr << std::endl;
    }
    std::cerr << "Input coordinates (2) for " << nd << " monomers:\n";
    for (size_t i = 0; i < nd; i++) {
        for (size_t j = 0; j < 3; j++) {
            std::cerr << x[3 * i + j] << " , ";
        }
        std::cerr << std::endl;
    }
    std::cerr << "Input gradients (1) for " << nd << " monomers:\n";
    for (size_t i = 0; i < nd; i++) {
        for (size_t j = 0; j < 9; j++) {
            std::cerr << g1[9 * i + j] << " , ";
        }
        std::cerr << std::endl;
    }
    std::cerr << "Input gradients (2) for " << nd << " monomers:\n";
    for (size_t i = 0; i < nd; i++) {
        for (size_t j = 0; j < 3; j++) {
            std::cerr << g2[3 * i + j] << " , ";
        }
        std::cerr << std::endl;
    }
    std::cerr << "Input virial:\n";
    for (size_t i = 0; i < 9; i++) {
        std::cerr << (*virial)[i] << " , ";
    }
    std::cerr << std::endl;
#endif

    std::vector<size_t> dimers_todo;
    std::vector<double> energy(nd, 0.0);
    double rabsq[nd];
    double rab[nd];
    double dab[3 * nd];
    for (size_t i = 0; i < nd; i++) {
        dab[3 * i] = w1[9 * i + 0] - x[3 * i + 0];
        dab[3 * i + 1] = w1[9 * i + 1] - x[3 * i + 1];
        dab[3 * i + 2] = w1[9 * i + 2] - x[3 * i + 2];

        rabsq[i] = dab[3 * i + 0] * dab[3 * i + 0] + dab[3 * i + 1] * dab[3 * i + 1] + dab[3 * i + 2] * dab[3 * i + 2];
        rab[i] = std::sqrt(rabsq[i]);

        if (rab[i] > r2f) continue;

        dimers_todo.push_back(i);
    }

    size_t ndtd = dimers_todo.size();
    double vv[8 * ndtd];
    double gg[8 * ndtd];
    variable ctxt[8 * ndtd];
    monomer m[ndtd];
    double e = 0.0;

    for (size_t i = 0; i < ndtd; i++) {
        // offsets
        size_t sh = dimers_todo[i];
        size_t sh3 = sh * 3;
        size_t sh9 = sh3 * 3;

        const int O = 0;
        const int H1 = 3;
        const int H2 = 6;

        const int X = 9;

        const int lp1 = 12;
        const int lp2 = 15;

        double xcrd[18];  // coordinates including extra-points

        std::copy(w1 + sh9, w1 + sh9 + 9, xcrd);
        std::copy(x + sh3, x + sh3 + 3, xcrd + 9);

        // Set lone pairs
        m[i].setup(xcrd + O, in_plane_gamma, out_of_plane_gamma, xcrd + lp1, xcrd + lp2);

        // Get the polynomial monomials
        double v[8];

        v[0] = ctxt[i * 8 + 0].v_exp(d_HH_intra, k_HH_intra, xcrd, H1, H2);

        v[1] = ctxt[i * 8 + 1].v_exp(d_OH_intra, k_OH_intra, xcrd, O, H1);
        v[2] = ctxt[i * 8 + 2].v_exp(d_OH_intra, k_OH_intra, xcrd, O, H2);

        v[3] = ctxt[i * 8 + 3].v_coul(d_XH_coul, k_XH_coul, xcrd, X, H1);
        v[4] = ctxt[i * 8 + 4].v_coul(d_XH_coul, k_XH_coul, xcrd, X, H2);

        v[5] = ctxt[i * 8 + 5].v_coul(d_XO_coul, k_XO_coul, xcrd, X, O);

        v[6] = ctxt[i * 8 + 6].v_exp(d_xlp_main, k_xlp_main, xcrd, X, lp1);
        v[7] = ctxt[i * 8 + 7].v_exp(d_xlp_main, k_xlp_main, xcrd, X, lp2);

        for (size_t j = 0; j < 8; j++) {
            vv[j * dimers_todo.size() + i] = v[j];
        }
    }

    std::vector<double> e2b = poly_2b_h2o_ion_v1x::eval(ndtd, twobodyfit.data(), vv, gg);

    for (size_t i = 0; i < ndtd; i++) {
        // offsets
        size_t sh = dimers_todo[i];
        size_t sh3 = sh * 3;
        size_t sh9 = sh3 * 3;

        const int O = 0;
        const int H1 = 3;
        const int H2 = 6;

        const int X = 9;

        const int lp1 = 12;
        const int lp2 = 15;

        double xgrd[18];
        std::fill(xgrd, xgrd + 18, 0.0);

        ctxt[i * 8 + 0].grads(gg[0 * ndtd + i], xgrd, H1, H2);

        ctxt[i * 8 + 1].grads(gg[1 * ndtd + i], xgrd, O, H1);
        ctxt[i * 8 + 2].grads(gg[2 * ndtd + i], xgrd, O, H2);

        ctxt[i * 8 + 3].grads(gg[3 * ndtd + i], xgrd, X, H1);
        ctxt[i * 8 + 4].grads(gg[4 * ndtd + i], xgrd, X, H2);

        ctxt[i * 8 + 5].grads(gg[5 * ndtd + i], xgrd, X, O);

        ctxt[i * 8 + 6].grads(gg[6 * ndtd + i], xgrd, X, lp1);
        ctxt[i * 8 + 7].grads(gg[7 * ndtd + i], xgrd, X, lp2);

        m[i].grads(xgrd + lp1, xgrd + lp2, in_plane_gamma, out_of_plane_gamma, xgrd + O);

        double gsw;
        const double sw = f_switch(rab[sh], gsw);

        for (size_t j = 0; j < 9; j++) {
            g1[j + sh9] += sw * xgrd[j];
        }
        for (size_t j = 0; j < 3; j++) {
            g2[j + sh3] += sw * xgrd[j + 9];
        }

        // gradient of the switch
        // MRR Careful here. If g1 and g2 are not initialized to 0 before the function call, the virial might not be
        // correct

        gsw *= e2b[i] / rab[sh];
        for (size_t j = 0; j < 3; ++j) {
            const double d = gsw * dab[sh3 + j];
            g1[j + sh9] += d;
            g2[j + sh3] -= d;
        }

        if (virial != 0) {
            (*virial)[0] -= w1[0 + 0 + sh9] * g1[0 + 0 + sh9] + w1[0 + 3 + sh9] * g1[0 + 3 + sh9] +
                            w1[0 + 6 + sh9] * g1[0 + 6 + sh9] + x[0 + 0 + sh3] * g2[0 + 0 + sh3];

            (*virial)[1] -= w1[0 + 0 + sh9] * g1[0 + 1 + sh9] + w1[0 + 3 + sh9] * g1[0 + 4 + sh9] +
                            w1[0 + 6 + sh9] * g1[0 + 7 + sh9] + x[0 + 0 + sh3] * g2[0 + 1 + sh3];

            (*virial)[2] -= w1[0 + 0 + sh9] * g1[0 + 2 + sh9] + w1[0 + 3 + sh9] * g1[0 + 5 + sh9] +
                            w1[0 + 6 + sh9] * g1[0 + 8 + sh9] + x[0 + 0 + sh3] * g2[0 + 2 + sh3];

            (*virial)[4] -= w1[0 + 1 + sh9] * g1[0 + 1 + sh9] + w1[0 + 4 + sh9] * g1[0 + 4 + sh9] +
                            w1[0 + 7 + sh9] * g1[0 + 7 + sh9] + x[0 + 1 + sh3] * g2[0 + 1 + sh3];

            (*virial)[5] -= w1[0 + 1 + sh9] * g1[0 + 2 + sh9] + w1[0 + 4 + sh9] * g1[0 + 5 + sh9] +
                            w1[0 + 7 + sh9] * g1[0 + 8 + sh9] + x[0 + 1 + sh3] * g2[0 + 2 + sh3];

            (*virial)[8] -= w1[0 + 2 + sh9] * g1[0 + 2 + sh9] + w1[0 + 5 + sh9] * g1[0 + 5 + sh9] +
                            w1[0 + 8 + sh9] * g1[0 + 8 + sh9] + x[0 + 2 + sh3] * g2[0 + 2 + sh3];

            (*virial)[3] = (*virial)[1];
            (*virial)[6] = (*virial)[2];
            (*virial)[7] = (*virial)[5];
        }

        e += sw * e2b[i];
    }

#ifdef DEBUG
    std::cerr << std::scientific << std::setprecision(10);
    std::cerr << "\nExiting " << __func__ << " in " << __FILE__ << std::endl;
    std::cerr << "Output energy: " << e << std::endl;
    std::cerr << "Output gradients (1) for " << nd << " monomers:\n";
    for (size_t i = 0; i < nd; i++) {
        for (size_t j = 0; j < 9; j++) {
            std::cerr << g1[9 * i + j] << " , ";
        }
        std::cerr << std::endl;
    }
    std::cerr << "Output gradients (2) for " << nd << " monomers:\n";
    for (size_t i = 0; i < nd; i++) {
        for (size_t j = 0; j < 3; j++) {
            std::cerr << g2[3 * i + j] << " , ";
        }
        std::cerr << std::endl;
    }
    std::cerr << "Output virial:\n";
    for (size_t i = 0; i < 9; i++) {
        std::cerr << (*virial)[i] << " , ";
    }
    std::cerr << std::endl;
#endif

    return e;
}

